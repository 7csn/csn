<?php

namespace csn;

class StdClass
{

    // ----------------------------------------------------------------------
    //  构造函数
    // ----------------------------------------------------------------------

    protected function __construct()
    {
    }

    // ----------------------------------------------------------------------
    //  单例
    // ----------------------------------------------------------------------

    protected static $single;

    static function single()
    {
        return is_null(self::$single) ? self::$single = new self : self::$single;
    }

    // ----------------------------------------------------------------------
    //  实例
    // ----------------------------------------------------------------------

    static function instance()
    {
        return new self;
    }

}