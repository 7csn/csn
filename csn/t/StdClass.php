<?php

namespace csn;

class StdClass extends Instance
{

    // ----------------------------------------------------------------------
    //  匿名函数
    // ----------------------------------------------------------------------

    protected $func;

    // ----------------------------------------------------------------------
    //  参数列表
    // ----------------------------------------------------------------------

    protected $args = [];

    // ----------------------------------------------------------------------
    //  构造函数：匿名函数、默认参数列表为空
    // ----------------------------------------------------------------------

    function construct($func)
    {
        $this->func = $func;
        return false;
    }

    // ----------------------------------------------------------------------
    //  设置参数
    // ----------------------------------------------------------------------

    function args()
    {
        $this->args = func_get_args();
        return $this;
    }

    function onceArgs($args)
    {
        $this->args = $args;
        return $this;
    }

    // ----------------------------------------------------------------------
    //  执行匿名函数
    // ----------------------------------------------------------------------

    function run()
    {
        return call_user_func_array($this->func, $this->args);
    }

}